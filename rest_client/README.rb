
Make sure that you've got Rest Client installed, do a $ gem install rest-client
Head into IRB ($ irb)
require 'rest-client'
Now you're able to play with the gem. Read its documentation over in the Github repo HERE. You'll end up doing this a whole lot -- using a new gem and going to its repo to check out the documentation, so get familiar with the process early!
Use RestClient to do a Bing search (yes, Bing) and examine the results (you can find the parameters that Bing wants in the URL by simply making a search in a normal browser. Pay attention to the q= parameter).
